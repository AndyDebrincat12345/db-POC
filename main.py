import os
import mysql.connector
from dotenv import load_dotenv
import subprocess
import time
from datetime import datetime

load_dotenv()

class InteractiveMigrationTester:
    def __init__(self):
        self.db_config = {
            'host': os.getenv("DB_HOST", "localhost"),
            'port': int(os.getenv("DB_PORT", 3306)),
            'user': os.getenv("DB_USER"),
            'password': os.getenv("DB_PASS"),
            'database': os.getenv("DB_NAME")
        }
    
    def get_connection(self):
        try:
            return mysql.connector.connect(**self.db_config)
        except mysql.connector.Error as err:
            print(f"‚ùå Database connection failed: {err}")
            return None

    def run_sql_file(self, cursor, filepath):
        """Execute SQL file with proper statement splitting"""
        with open(filepath, "r") as f:
            sql = f.read()
        
        # Split by semicolon for MySQL
        statements = [stmt.strip() for stmt in sql.split(";") if stmt.strip()]
        
        for statement in statements:
            try:
                cursor.execute(statement)
            except mysql.connector.Error as err:
                print(f"‚ùå Error executing statement: {err}")
                print(f"Statement preview: {statement[:100]}...")
                raise

    def run_liquibase_migrations(self):
        """Run Liquibase migrations via CLI"""
        print("üü° Running Liquibase migrations...")
        start_time = time.time()
        
        try:
            # Determine correct command for platform
            liquibase_cmd = "liquibase.bat" if os.name == 'nt' else "liquibase"
            
            result = subprocess.run(
                [liquibase_cmd, "--defaultsFile=liquibase.properties", "update"],
                cwd="liquibase",
                capture_output=True,
                text=True,
                timeout=120
            )
            
            execution_time = time.time() - start_time
            
            if result.returncode == 0:
                print(f"‚úÖ Liquibase completed successfully in {execution_time:.2f}s")
                if result.stdout:
                    print("üìù Output:", result.stdout[-200:])  # Show last 200 chars
            else:
                print(f"‚ùå Liquibase failed: {result.stderr}")
                
        except subprocess.TimeoutExpired:
            print("‚è∞ Liquibase command timed out")
        except FileNotFoundError:
            print("‚ùå Liquibase executable not found. Please install Liquibase CLI.")
        except Exception as err:
            print(f"‚ùå Liquibase error: {err}")

    def run_migration_folder(self, cursor, conn, folder_path, tool_name):
        """Run SQL migrations from a folder"""
        print(f"üìÅ Running {tool_name} migrations from {folder_path}...")
        start_time = time.time()
        
        try:
            if not os.path.exists(folder_path):
                print(f"‚ùå Migration folder not found: {folder_path}")
                return
            
            files = sorted([f for f in os.listdir(folder_path) if f.endswith(".sql")])
            
            if not files:
                print(f"‚ùå No SQL files found in {folder_path}")
                return
            
            print(f"üìã Found {len(files)} migration files")
            
            for filename in files:
                filepath = os.path.join(folder_path, filename)
                print(f"  üìÑ Running {filename}...")
                try:
                    self.run_sql_file(cursor, filepath)
                    conn.commit()
                    print(f"  ‚úÖ {filename} completed successfully")
                except mysql.connector.Error as err:
                    print(f"  ‚ùå {filename} failed: {err}")
                    conn.rollback()
                    return
            
            execution_time = time.time() - start_time
            print(f"‚úÖ {tool_name} migrations completed in {execution_time:.2f}s")
            
        except Exception as err:
            print(f"‚ùå Error running {tool_name} migrations: {err}")

    def reset_database(self, cursor, conn):
        """Reset database to clean state"""
        print("üîÑ Resetting database...")
        try:
            # Drop all tables to start fresh
            cursor.execute("SET FOREIGN_KEY_CHECKS = 0")
            cursor.execute("SHOW TABLES")
            tables = cursor.fetchall()
            
            if tables:
                print(f"  üóëÔ∏è  Dropping {len(tables)} existing tables...")
                for (table,) in tables:
                    cursor.execute(f"DROP TABLE {table}")
                    print(f"  ‚ùå Dropped table: {table}")
            else:
                print("  ‚ÑπÔ∏è  No tables to drop")
            
            cursor.execute("SET FOREIGN_KEY_CHECKS = 1")
            conn.commit()
            print("  ‚úÖ Database reset completed")
            
        except mysql.connector.Error as err:
            print(f"  ‚ùå Database reset failed: {err}")

    def analyze_database_state(self, cursor):
        """Analyze current database state"""
        print("\nüìä Current Database State:")
        try:
            # Get table count
            cursor.execute("SHOW TABLES")
            tables = cursor.fetchall()
            print(f"  üìã Total tables: {len(tables)}")
            
            if not tables:
                print("  ‚ÑπÔ∏è  No tables found in database")
                return
            
            # Show table names
            table_names = [table[0] for table in tables]
            print(f"  üìù Tables: {', '.join(table_names)}")
            
            # Count records in key tables
            key_tables = ['users', 'users_comprehensive', 'users_redgate', 'products', 'categories', 'roles', 'permissions']
            
            for table in key_tables:
                if table in table_names:
                    try:
                        cursor.execute(f"SELECT COUNT(*) FROM {table}")
                        count = cursor.fetchone()[0]
                        print(f"  üìä {table}: {count} records")
                    except mysql.connector.Error as err:
                        print(f"  ‚ùå Error reading {table}: {err}")
            
        except mysql.connector.Error as err:
            print(f"  ‚ùå Database analysis failed: {err}")

    def view_table_data(self, cursor, table_name, limit=10):
        """View data from a specific table"""
        try:
            cursor.execute(f"SELECT * FROM {table_name} LIMIT {limit}")
            rows = cursor.fetchall()
            
            if not rows:
                print(f"  ‚ÑπÔ∏è  Table '{table_name}' is empty")
                return
            
            # Get column names
            cursor.execute(f"DESCRIBE {table_name}")
            columns = [row[0] for row in cursor.fetchall()]
            
            print(f"\nüìã {table_name.capitalize()} (showing up to {limit} records):")
            print("  " + " | ".join(f"{col:15}" for col in columns))
            print("  " + "-" * (len(columns) * 17))
            
            for row in rows:
                formatted_row = []
                for item in row:
                    if item is None:
                        formatted_row.append("NULL")
                    elif isinstance(item, str) and len(item) > 15:
                        formatted_row.append(item[:12] + "...")
                    else:
                        formatted_row.append(str(item))
                print("  " + " | ".join(f"{item:15}" for item in formatted_row))
            
        except mysql.connector.Error as err:
            print(f"  ‚ùå Error viewing table '{table_name}': {err}")

    def insert_test_user(self, cursor, conn):
        """Insert a test user into the appropriate users table"""
        print("\nüë§ Insert Test User")
        username = input("  Enter username: ").strip()
        email = input("  Enter email: ").strip()
        first_name = input("  Enter first name (optional): ").strip() or None
        last_name = input("  Enter last name (optional): ").strip() or None
        
        # Try to insert into the most comprehensive users table available
        tables_to_try = ['users_comprehensive', 'users_redgate', 'users']
        
        for table in tables_to_try:
            try:
                cursor.execute("SHOW TABLES LIKE %s", (table,))
                if cursor.fetchone():
                    if table == 'users_comprehensive':
                        cursor.execute(
                            "INSERT INTO users_comprehensive (username, email, first_name, last_name) VALUES (%s, %s, %s, %s)",
                            (username, email, first_name, last_name)
                        )
                    elif table == 'users_redgate':
                        cursor.execute(
                            "INSERT INTO users_redgate (username, email, first_name, last_name) VALUES (%s, %s, %s, %s)",
                            (username, email, first_name, last_name)
                        )
                    else:
                        cursor.execute(
                            "INSERT INTO users (username, email) VALUES (%s, %s)",
                            (username, email)
                        )
                    
                    conn.commit()
                    print(f"  ‚úÖ User added to {table}")
                    return
                    
            except mysql.connector.Error as err:
                print(f"  ‚ùå Error inserting into {table}: {err}")
                continue
        
        print("  ‚ùå No suitable users table found")

